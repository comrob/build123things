from random import random

"""
Provide RGB color constants and a colors dictionary with
elements formatted: colors[colorname] = CONSTANT

Reused from https://www.webucator.com/article/python-color-constants-module/
"""
class Color:
    """ Each element in range 0..1. """

    def __init__(self, r, g, b, a) -> None:
        self.rgba = (r, g, b, a)
        assert all(map(lambda x : 0.0 <= x <= 1.0, self.rgba)), f"{r}, {g}, {b}, {a}"

    @property
    def r (self):
        return self.rgba[0]

    @property
    def g (self):
        return self.rgba[1]

    @property
    def b (self):
        return self.rgba[2]

    @property
    def a (self):
        return self.rgba[3]

class RGBA_Scaled (Color):
    def __init__(self, r, g, b, a = None, scale=256) -> None:
        if a is None:
            a = scale
        super().__init__(r/scale, g/scale, b/scale, a/scale)

class RandomColor (Color):
    def __init__(self, a:float=1, **kwargs) -> None:
        assert 0.0 <= a <= 1.0
        mn = [0,0,0]
        mx = [1,1,1]
        for name, value in kwargs.items():
            assert 0.0 <= value <= 1.0
            c, what = name.split("__")
            c = {"r":0,"g":1,"b":2,0:0,1:1,2:2}[c.lower()]
            what = {"min":mn, "max":mx}[what.lower()]
            what[c] = value
        v = [x[0] + random() * (x[1] - x[0]) for x in zip(mn, mx)]
        super().__init__(v[0], v[1], v[2], a)

